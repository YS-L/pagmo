#include <exception>
#include <cstdlib>
#include "../exceptions.h"
#include <iostream>

#include "neural_network.h"


using namespace ann_toolbox;

neural_network::neural_network(unsigned int input_nodes_, unsigned int output_nodes_):
	m_inputs(input_nodes_), m_outputs(output_nodes_)
{}

neural_network::~neural_network() {}

void neural_network::set_weights(const std::vector<CUDA_TY> &chromosome) 
{
  if(chromosome.size() != m_weights.size())
    {
      std::cout<<m_weights.size()<<" "<<chromosome.size();
      pagmo_throw(value_error, "number of weights is incorrect");
    }
  m_weights = chromosome;
}

void neural_network::print()
{
  print("weights", m_weights);
}

void neural_network::print(const char * message, std::vector<CUDA_TY> & c)
{
  std::cout<<message<<":";
  for (int i=0; i<c.size(); ++i)
  {
    std::cout<<c[i]<<" ";
  }
  std::cout<<std::endl;
}
